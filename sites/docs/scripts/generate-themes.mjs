// Generates src/lib/themes-data.ts from the shipped preset CSS in
// packages/svelte-docsmith/src/lib/themes/*.css. The CSS files are the single
// source of truth for the presets; the showcase reads this generated mirror so
// it renders exactly what a consumer gets, with no hand-copied values to drift.
//
// Runs automatically before `dev` and `build` (see package.json); run it by hand
// with `npm run gen:themes`.

import { readFileSync, writeFileSync, readdirSync } from 'node:fs';
import { dirname, join, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const here = dirname(fileURLToPath(import.meta.url));
const themesDir = resolve(here, '../../../packages/svelte-docsmith/src/lib/themes');
const outFile = resolve(here, '../src/lib/themes-data.ts');

// Picker/display order: default first, then warm-to-cool-to-neutral, matching the
// order the README and docs list them in.
const ORDER = [
	'darkmatter',
	'tangerine',
	'amethyst',
	'graphite',
	'evergreen',
	'rose',
	'ocean',
	'nord',
	'claude',
	'bubblegum',
	'mono'
];

const DEFAULT_SLUG = 'darkmatter';

/** Parse every `--token: value;` declaration inside a selector block. */
function parseBlock(css, selector) {
	const re = new RegExp(`${selector}\\s*\\{([^}]*)\\}`);
	const body = css.match(re)?.[1] ?? '';
	const tokens = {};
	for (const [, name, value] of body.matchAll(/--([\w-]+):\s*([^;]+);/g)) {
		tokens[name] = value.trim();
	}
	return tokens;
}

/** Pull the display name out of the header comment `theme — <Name>`. */
function parseName(css, slug) {
	const match = css.match(/theme —\s*([^.\n(]+)/);
	return match ? match[1].trim() : slug;
}

const files = readdirSync(themesDir).filter((f) => f.endsWith('.css'));
const bySlug = new Map();
for (const file of files) {
	const slug = file.replace(/\.css$/, '');
	const css = readFileSync(join(themesDir, file), 'utf8');
	bySlug.set(slug, {
		name: parseName(css, slug),
		slug,
		light: parseBlock(css, ':root'),
		dark: parseBlock(css, '\\.dark')
	});
}

// Emit in the curated order; append any preset not listed (so a new file still
// shows up) alphabetically after the known ones.
const ordered = [
	...ORDER.filter((s) => bySlug.has(s)),
	...[...bySlug.keys()].filter((s) => !ORDER.includes(s)).sort()
].map((slug) => bySlug.get(slug));

const banner = `// AUTO-GENERATED by scripts/generate-themes.mjs from the shipped preset CSS in
// packages/svelte-docsmith/src/lib/themes/*.css. Do not edit by hand — change the
// CSS and re-run \`npm run gen:themes\` (which also runs before dev and build).
//
// Each entry carries a preset's full token set (colors, charts, sidebar, radius)
// for both modes, so the showcase applies exactly what the shipped CSS would.`;

const body = `${banner}

export type ThemeTokens = Record<string, string>;
export type Theme = { name: string; slug: string; light: ThemeTokens; dark: ThemeTokens };

export const themes: Theme[] = ${JSON.stringify(ordered, null, '\t')};

export const defaultThemeSlug = '${DEFAULT_SLUG}';
`;

writeFileSync(outFile, body);
console.log(`Generated ${outFile} from ${ordered.length} presets.`);
